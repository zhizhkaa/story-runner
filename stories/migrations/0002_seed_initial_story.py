def seed_story(apps, schema_editor):
    StoryTemplate = apps.get_model("stories", "StoryTemplate")
    TemplateNode = apps.get_model("stories", "TemplateNode")
    template = StoryTemplate.objects.create(name="Основная User Story")

    def add_nodes(items, parent=None):
        for position, item in enumerate(items, start=1):
            code, title, *rest = item
            children = rest[0] if rest else None
            node = TemplateNode.objects.create(
                template=template,
                parent=parent,
                code=code,
                title=title,
                kind="group" if children else "check",
                position=position,
            )
            if children:
                add_nodes(children, node)

    add_nodes(STORY)


